package com.reliance.gstn.util;

public class InvalidSession {

	private String isSessionValid;
	
	private String isTokenValid;
	
	private String isXSSValid;

	public String getIsSessionValid() {
		return isSessionValid;
	}

	public void setIsSessionValid(String isSessionValid) {
		this.isSessionValid = isSessionValid;
	}

	public String getIsTokenValid() {
		return isTokenValid;
	}

	public void setIsTokenValid(String isTokenValid) {
		this.isTokenValid = isTokenValid;
	}

	public String getIsXSSValid() {
		return isXSSValid;
	}

	public void setIsXSSValid(String isXSSValid) {
		this.isXSSValid = isXSSValid;
	}
	
	
	
	
}
